export function SocialsSection({ config }: { config: any }) {
  if (config.isHidden) return null;

  const getIcon = (url: string) => {
    if (url.includes("facebook")) return <FacebookIcon className="w-6 h-6" />;
    if (url.includes("twitter") || url.includes("x.com")) return <TwitterIcon className="w-6 h-6" />;
    if (url.includes("instagram")) return <InstagramIcon className="w-6 h-6" />;
    if (url.includes("linkedin")) return <LinkedinIcon className="w-6 h-6" />;
    if (url.includes("youtube")) return <YoutubeIcon className="w-6 h-6" />;
    if (url.includes("tiktok")) return <span className="font-bold">TikTok</span>;
    return <span className="font-bold">Link</span>;
  };

  const links = [
    { url: config.facebook, hidden: config.hideFacebook },
    { url: config.twitter, hidden: config.hideTwitter },
    { url: config.instagram, hidden: config.hideInstagram },
    { url: config.linkedin, hidden: config.hideLinkedin },
    { url: config.youtube, hidden: config.hideYoutube },
    { url: config.tiktok, hidden: config.hideTiktok },
  ].filter(link => link.url && !link.hidden);

  return (
    <section className="py-16 bg-white dark:bg-zinc-950">
      <div className="max-w-4xl mx-auto px-6 text-center">
        {!config.hideTitle && config.title && (
          <h2 className="text-2xl font-bold text-zinc-900 dark:text-white mb-8">{config.title}</h2>
        )}
        
        {links.length > 0 ? (
          <div className="flex justify-center gap-6 flex-wrap">
            {links.map((link, idx) => (
              <a
                key={idx}
                href={link.url}
                target="_blank"
                rel="noopener noreferrer"
                className="w-12 h-12 rounded-full bg-zinc-100 dark:bg-zinc-800 flex items-center justify-center text-zinc-600 dark:text-zinc-400 hover:bg-indigo-100 dark:hover:bg-indigo-900/30 hover:text-indigo-600 dark:hover:text-indigo-400 transition-colors"
              >
                {getIcon(link.url)}
              </a>
            ))}
          </div>
        ) : (
          <p className="text-zinc-500 text-sm">No social links configured</p>
        )}
      </div>
    </section>
  );
}
